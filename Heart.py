import turtle

turtle.speed(5)
turtle.bgcolor('cornsilk1')
turtle.pensize(6)

def heart():
    for i in range(200):
        turtle.right(1)
        turtle.forward(1)
turtle.color('red','pink')
#turtle.color('off-white','lavender')
turtle.begin_fill()
turtle.left(140)
turtle.forward(111.65)
heart()
turtle.left(120)
heart()
turtle.forward(111.65)
turtle.end_fill()
turtle.hideturtle()
turtle.done()




